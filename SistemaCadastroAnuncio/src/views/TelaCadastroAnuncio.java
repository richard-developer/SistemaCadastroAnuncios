    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        // Verfifica se o usuário preencheu todos os campos:
        if (txtNomeAnuncio.getText() == null || txtNomeAnuncio.getText().trim().equals("")) {
            txtNomeAnuncioVazio.setText("Nome do Anúncio vazio.");

        } else {
            txtNomeAnuncioVazio.setVisible(false);
        }

        if (txtCliente.getText() == null || txtCliente.getText().trim().equals("")) {
            txtClienteVazio.setText("Nome do Cliente vazio.");

        } else {
            txtClienteVazio.setVisible(false);
        }

        if (txtValorDia.getText() == null || txtValorDia.getText().trim().equals("")) {
            txtValorDiaVazio.setText("Investimento por Dia vazio.");

        } else {
            txtValorDiaVazio.setVisible(false);

        }

        if ("  /  /    ".equals(txtDataInicio.getText()) || "  /  /    ".equals(txtDataTermino.getText())) {

            JOptionPane.showMessageDialog(null, "Favor preencher ambas as Datas.", "Erro", JOptionPane.ERROR_MESSAGE);

        } else {

            Anuncio a = new Anuncio();
            AnuncioDAO dao = new AnuncioDAO();

            // Formatação dos campos de Data de Início e Término:   
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate dataInicio = LocalDate.parse(txtDataInicio.getText(), formato);
            LocalDate dataTermino = LocalDate.parse(txtDataTermino.getText(), formato);

            // Compara se a Data Término é menor ou igual a Data Início:
            if (dataTermino.compareTo(dataInicio) <= 0) {

                JOptionPane.showMessageDialog(null, "Data Término tem que ser maior que Data Início.", "Erro", JOptionPane.ERROR_MESSAGE);

            } else {
                // Pegando os valores do Formulário:
                a.setNOME_ANUNCIO(txtNomeAnuncio.getText());
                a.setCLIENTE(txtCliente.getText());
                a.setDATA_INICIO(dataInicio);
                a.setDATA_TERMINO(dataTermino);
                a.setVALOR_INVEST_DIA(Integer.parseInt(txtValorDia.getText()));

                // Envia os dados do Formuário para a Classe AnuncioDAO:
                dao.create(a);

                // Lê o que foi cadastrado no Banco:
                readJTable();

                // Limpa os campos do formulário para um novo cadastro:
                txtNomeAnuncio.setText("");
                txtCliente.setText("");
                txtDataInicio.setText("");
                txtDataTermino.setText("");
                txtValorDia.setText("");

                // Limpa os campos de erro:
                txtNomeAnuncioVazio.setText("");
                txtClienteVazio.setText("");
                txtValorDiaVazio.setText("");
            }

        }


    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnGerarRetorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGerarRetorioActionPerformed

        // Verifica se o usuário selecionou alguma linha na Tabela: 
        if (jTAnuncios.getSelectedRow() == - 1) {
            JOptionPane.showMessageDialog(null, "Favor selecionar um Anúncio Cadastrado para Gerar o Relatório.");

        } else {

            // Pega os dados vindos da Tabela jTAnuncios:
            txtIdRelatorio.setText(jTAnuncios.getValueAt(jTAnuncios.getSelectedRow(), 0).toString());
            txtNomeAnuncioRelatorio.setText(jTAnuncios.getValueAt(jTAnuncios.getSelectedRow(), 1).toString());
            txtClienteRelatorio.setText(jTAnuncios.getValueAt(jTAnuncios.getSelectedRow(), 2).toString());

            // Formatando as Datas vindas da Tabela JTAnuncios:
            DateTimeFormatter formatoDesejado = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate dataInicioRelatorio = LocalDate.parse(jTAnuncios.getValueAt(jTAnuncios.getSelectedRow(), 3).toString());
            LocalDate dataTerminoRelatorio = LocalDate.parse(jTAnuncios.getValueAt(jTAnuncios.getSelectedRow(), 4).toString());

            txtDataInicioRelatorio.setText(formatoDesejado.format(dataInicioRelatorio));
            txtDataTerminoRelatorio.setText(formatoDesejado.format(dataTerminoRelatorio));

            txtValorDiaRelatorio.setText(jTAnuncios.getValueAt(jTAnuncios.getSelectedRow(), 5).toString());

            // Calcula o período entre as datas:
            Period periodo = Period.between(dataInicioRelatorio, dataTerminoRelatorio);
            int dias = periodo.getDays();
            int meses = periodo.getMonths();

            // Cálculo do período Total de Dias, convencionando cada mês com 30 dias:
            int qtdeTotalDias = dias + (meses * 30);

            int valorDia = Integer.parseInt(txtValorDiaRelatorio.getText());

            // Cálculo do Valor Total Investido, com base no período do Anúncio:
            int valorTotalInvestido = qtdeTotalDias * valorDia;

            /* Com base no cálculo da Calculadora de Anúncio, chegou-se a seguinte conclusão:
           Com R$ 1 investido, tem-se uma quantidade máxima aproximada de 116 visualizações por dia,
           considerando o anúncio original + compartilhamentos.*/
            int qtdeMinVisual = 116;

            int qtdeMaxVisual = (valorTotalInvestido * qtdeMinVisual) * qtdeTotalDias;

            // Com R$ 1 investido, tem-se uma quantidade máxima de 4 cliques por dia:
            int qtdeMinCliques = 4;

            // Cálculo da Quantidade Máxima de Cliques:
            int qtdeMaxCliqes = (valorTotalInvestido * qtdeMinCliques) * qtdeTotalDias;

            // Com R4 1 investido, tem-se a quantidade de 0.54 compartilhamentos:
            float qtdeMinCompart = (float) 0.54;

            // Cálculo da Quantidade Máxima Aproximada de Compartilhamentos:
            int qtdeMaxCompart = Math.round(valorTotalInvestido * qtdeMinCompart) * qtdeTotalDias;

            // Insere os valores caculados nos campos do Relatório:
            txtTotalInvestido.setText(String.valueOf(valorTotalInvestido));
            txtQtdeMaxVisual.setText(String.valueOf(qtdeMaxVisual));
            txtQtdeMaxCliques.setText(String.valueOf(qtdeMaxCliqes));
            txtQtdeMaxCompart.setText(String.valueOf(qtdeMaxCompart));

        }


    }//GEN-LAST:event_btnGerarRetorioActionPerformed

