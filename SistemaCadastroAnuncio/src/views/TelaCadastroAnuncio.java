    public void readJTable() {

        // Pega o Modelo da Tabela:
        DefaultTableModel modelo = (DefaultTableModel) jTAnuncios.getModel();

        modelo.setNumRows(0); // Evita a dublicação na exibição dos dados na tabela.

        AnuncioDAO adao = new AnuncioDAO();

        // Laço de repetição para percorrer a Lista:
        for (Anuncio a : adao.read()) {

            // Adiciona os dados do Banco de Dados na Tabela JTAnuncios:
            modelo.addRow(new Object[]{
                a.getID(),
                a.getNOME_ANUNCIO(),
                a.getCLIENTE(),
                a.getDATA_INICIO_BD(),
                a.getDATA_TERMINO_BD(),
                a.getVALOR_INVEST_DIA()

            });
        }

    }

